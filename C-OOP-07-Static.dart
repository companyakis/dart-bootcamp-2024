//calculus.dart file

//static usage

class Calculus {
  
  static const double pi = 3.14;

  static const int startYear = 1923;

  static double sum(double a, double b) {
    return a + b;
  }

  static diff(double a, double b) {
    return a - b;
  }

  static mult(double a, double b) {
    return a * b;
  }

  static divide(double a, double b) {
    return a / b;
  }
}
